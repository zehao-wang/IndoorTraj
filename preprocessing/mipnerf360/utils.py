def convertRTtoTransformMatrix(R, T):
    w2c = np.eye(4)
    w2c[:3, 3] = T
    w2c[:3,:3] = np.transpose(R)
    c2w = np.linalg.inv(w2c)
    c2w[:3, 1:3] *= -1
    return c2w
